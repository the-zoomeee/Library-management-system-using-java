    private void issueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueActionPerformed
        // TODO add your handling code here:
        String bkId = bookId.getText();
        String stdId = studentId.getText();
        
        try
        {
            String sql = null;
            stmt = conn.createStatement();
            sql = "update issue set returnBook='Yes' where bookID = '"+bkId+"' and studentID = '"+stdId+"'";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Book Returned Successfully!!");
            setVisible(false);
            new returnBook().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Connection error!!");
            setVisible(false);
            new newBook().setVisible(true);
        }
        
    }//GEN-LAST:event_issueActionPerformed

    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // TODO add your handling code here:
        String bkId = bookId.getText();
        String stdId = studentId.getText();
        
        try
        {
            String sql = null;
            stmt = conn.createStatement();
            sql = "select * from issue where bookId = '"+bkId+"' and studentID = '"+stdId+"'";
            rs = stmt.executeQuery(sql);
            
            if(rs.next())
            {
                issueDate.setText(rs.getString(3));
                dueDate.setText(rs.getString(4));
                bookId.setEditable(false);
                studentId.setEditable(false);
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Book is NOT issued by this Student!!");
                setVisible(false);
                new returnBook().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Connection error!!");
            setVisible(false);
            new newBook().setVisible(true);
        }
    }//GEN-LAST:event_findActionPerformed

